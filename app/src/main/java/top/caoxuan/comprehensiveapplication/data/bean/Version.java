package top.caoxuan.comprehensiveapplication.data.bean;

public class Version {
    private String versionCode;
    private String versionName;
    private String updateDate;
    private String updateInfo;
    private String updateInfoDetail;

    public String getVersionCode() {
        return versionCode;
    }

    public void setVersionCode(String versionCode) {
        this.versionCode = versionCode;
    }

    public String getVersionName() {
        return versionName;
    }

    public void setVersionName(String versionName) {
        this.versionName = versionName;
    }

    public String getUpdateDate() {
        return updateDate;
    }

    public void setUpdateDate(String updateDate) {
        this.updateDate = updateDate;
    }

    public String getUpdateInfo() {
        return updateInfo;
    }

    public void setUpdateInfo(String updateInfo) {
        this.updateInfo = updateInfo;
    }

    public String getUpdateInfoDetail() {
        return updateInfoDetail;
    }

    public void setUpdateInfoDetail(String updateInfoDetail) {
        this.updateInfoDetail = updateInfoDetail;
    }
}
